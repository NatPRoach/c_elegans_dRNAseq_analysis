load("../../references/CPAT_Training/ce.logit.RData")
test <- read.table(file="../../results/coding_predictions/all_stringent_isoforms_predictions.tsv.dat",sep="\t",col.names=c("ID","mRNA","ORF","Fickett","Hexamer"))
